class Flower {
  final String flowerId;
  final String name;
  final String description;
  final double price;
  final String brand;
  final List<String> categories;
  final List<String> images;
  final List<String> colors;
  final List<String> tags;

  Flower(
      {this.flowerId,
      this.name,
      this.description,
      this.price,
      this.brand,
      this.categories,
      this.colors,
      this.images,
      this.tags});
}
